def playWaveOnBus(wav, loop, audioType, callback=None, refCon=None):
    data = wav.readframes(wav.getnframes())
    return playPCMOnBus(data,
                        (wav.getnframes() * wav.getsampwidth() * wav.getnchannels()),
                        wav.getsampwidth(),
                        wav.getframerate(),
                        wav.getnchannels(),
                        loop,
                        audioType,
                        callback,
                        refCon)
